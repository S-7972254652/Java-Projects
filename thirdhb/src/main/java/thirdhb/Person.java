package thirdhb;

public class Person {
private int id;
private String Name;
private String Address;
public int getid() {
	return id;
}
public void setid(int id) {
	this.id = id;
}
public String getName() {
	return Name;
}
public void setName(String Name) {
	this.Name = Name;
}
public String getAddress() {
	return Address;
}
public void setAddress(String Address) {
	this.Address = Address;
}


}
